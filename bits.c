/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
    int sign=x>>31;
    x=(~sign&x)|(sign&~x);/*如果x为正数则保持不变，如果x为负数则取反*/
    /*接下来检测数位上1的存在，初始检测区域为32位。
    每次检测都会选择包含更高位的那一半区域进行检测，若检测到此区域中存在1，则说明在表示原数时一定需要与此区域所含数位数量相同的数位，将此数量加进所需数位总数，并将检测区域缩小至此区域；
    若检测到此区域中不存在1，则说明在表示原数时不需要与此区域所含数位数量相同的数位，将检测区域缩小至原检测区域除去此区域的部分。
    一直检测到检测区域为1位*/
    int num1=!!(x>>16)<<4;
    x=x>>num1;
    int num2=!!(x>>8)<<3;
    x=x>>num2;
    int num3=!!(x>>4)<<2;
    x=x>>num3;
    int num4=!!(x>>2)<<1;
    x=x>>num4;
    int num5=!!(x>>1);
    x=x>>num5;
    int ans=num1+num2+num3+num4+num5+x+1;/*此时的x若为1则还需要一个数位，若为0则无需，符号位需要一个数位，所以还需将(x+1)加进所需数位总数*/
    return ans;
}
