/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
    int diff_lower=x+(~0x30+1);
    int diff_upper=x+(~0x39+1);
    int judge_lower=!(diff_lower>>31);/*判断(x+negate(0x30))是否大于等于0*/
    int judge_upper_1=!diff_upper;/*判断x是否等于0x39*/
    int judge_upper_2=diff_upper>>31;/*判断(x+negate(0x39))是否小于0*/
    int ans=judge_lower&(judge_upper_1|judge_upper_2);
    return ans;
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
    int cond=!!x<<31>>31;
    int ans=(cond&y)+(~cond&z);
    return ans;
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
    int sign=(x>>31)^(y>>31);/*判断x和y的符号位是否相同*/
    int diff=x+(~y+1);
    int judge=!!(diff>>31)|!diff;/*判断(x+negate(y))是否小于或等于0*/
    int ans=(sign&!!(x>>31))+(!sign&judge);/*符号不同时判断x是否为负数，符号相同时根据judge判断*/
    return ans;
}
