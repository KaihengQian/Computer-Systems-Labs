/*
 * isTmax - returns 1 if x is the maximum, two's complement number,
 *     and 0 otherwise 
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmax(int x) {
    int change_x=x+1;/*若x为Tmax,则x+1会正溢出得到Tmin，且Tmin的相反数与Tmin相等*/
    int ans=!(change_x^(~change_x+1))&(!!change_x);/*前者判断相反数是否与自身相等，后者排除x为-1的情况，因为x为-1时，change_x为0也符合前一条件*/
    return ans;
}
/* 
 * allOddBits - return 1 if all odd-numbered bits in word set to 1
 *   where bits are numbered from 0 (least significant) to 31 (most significant)
 *   Examples allOddBits(0xFFFFFFFD) = 0, allOddBits(0xAAAAAAAA) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allOddBits(int x) {
    int a=x&(x>>2);
    int b=a&(a>>4);
    int c=b&(b>>8);
    int d=c&(c>>16);
    /*对奇数位逐个检查是否为1，并将结果显示在d的倒数第二低的位上*/
    int ans=(d>>1)&0x1;/*若d的倒数第二位为1，则表示所有奇数位都为1*/
    return ans;
}
